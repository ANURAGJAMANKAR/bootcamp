"""
This script demonstrates basic list operations in Python:
- Appending a value
- Removing a value
- Sorting the list
"""

a = [5, 3, 8]

# Add the number 2 to the list
a.append(2)

# Remove the number 3 from the list
a.remove(3)

# Sort the list in ascending order
a.sort()

# Display the final state of the list
print("Sorted List:", a)
