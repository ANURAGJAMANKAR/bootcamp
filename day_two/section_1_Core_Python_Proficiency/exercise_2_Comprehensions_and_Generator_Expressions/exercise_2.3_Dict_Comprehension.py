"""
This script converts a list of strings into a dictionary
where each key maps to the value 1.
"""

keys = ["a", "b"]

# Create a dictionary with all values as 1
dictionary = {k: 1 for k in keys}

print("Dictionary:", dictionary)  # Output: {'a': 1, 'b': 1}
